    private void btn_TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TimKiemActionPerformed
        if(txt_TimKiem.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Mã phương tiện không được để trống!");
            return;
        }
        
        try{
            Vehicle v = sqlController.findById(txt_TimKiem.getText());
            List<Vehicle> listVehicle = new ArrayList<>();
            listVehicle.add(v);
            showData(listVehicle);
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, "Phương tiện không tìm thấy !");
        }
    }//GEN-LAST:event_btn_TimKiemActionPerformed

