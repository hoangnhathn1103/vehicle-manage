    public void showData(List<Vehicle> vehiclel){
        List<Vehicle> listVehicle = new ArrayList<>();
        listVehicle = vehiclel;
        DefaultTableModel tableModel;
        tbl_TrangChu.getModel();
        tableModel=(DefaultTableModel)tbl_TrangChu.getModel();
        tableModel.setRowCount(0);
        listVehicle.forEach((vehicle)->{
            tableModel.addRow(new Object[]{
                vehicle.getVehicleId(),vehicle.getVehicleName(),vehicle.getMaker(),vehicle.getUnitPrice(),vehicle.getYear(),vehicle.getColor(),vehicle.getSeatNum(),vehicle.getEngineType(),vehicle.getWattage(),vehicle.getTonnage()
            });
        });
    }

